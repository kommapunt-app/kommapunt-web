import { readdirSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const POSTERS_DIR = join(ROOT, "public/value-posters");
const OUT_FILE = join(ROOT, "lib/value-poster-map.ts");

const posterIds = readdirSync(POSTERS_DIR)
  .filter((filename) => filename.endsWith(".png"))
  .map((filename) => filename.slice(0, -".png".length))
  .sort();

const idsLiteral = posterIds.map((id) => JSON.stringify(id)).join(", ");

const contents = `// This file is auto-generated by scripts/generate-value-poster-map.mjs
// Add posters to public/value-posters/{valueId}.png, then run npm run generate:value-posters

export const VALUE_POSTER_IDS = [${idsLiteral}] as const;

export type ValuePosterId = (typeof VALUE_POSTER_IDS)[number];

export function hasValuePoster(valueId: string): valueId is ValuePosterId {
  return (VALUE_POSTER_IDS as readonly string[]).includes(valueId);
}

export function getValuePosterSrc(valueId: string): string | null {
  if (!hasValuePoster(valueId)) {
    return null;
  }

  return \`/value-posters/\${valueId}.png\`;
}
`;

writeFileSync(OUT_FILE, contents);

console.log(
  `Generated lib/value-poster-map.ts with ${posterIds.length} poster(s): ${posterIds.join(", ") || "(none)"}`,
);
